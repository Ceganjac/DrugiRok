    private void btnPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijavaActionPerformed

        // uzimanje podataka
        String mejl = txtMejl.getText();
        String lozinka = txtLozinka.getText();
        Admin admin = new Admin();
        admin.setMejl(mejl);
        admin.setLozinka(lozinka);

        Admin adminRez = null;

        try {
            adminRez = DbBroker.vratiIstancu().prijavaAdmin(admin);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        if (adminRez == null) {
            JOptionPane.showMessageDialog(this, "Nema korisnika sa datim kredencijalima. ",
                    "GREŠKA", JOptionPane.ERROR_MESSAGE);
            return;
        }

        dispose();
        GlavnaForma gf = new GlavnaForma(adminRez);
        gf.setExtendedState(MAXIMIZED_BOTH);
        gf.setVisible(true);

    }//GEN-LAST:event_btnPrijavaActionPerformed

